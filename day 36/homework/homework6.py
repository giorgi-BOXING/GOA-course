sentence = input("გთხოვთ, შეიყვანეთ წინადადება: ")


char = input("გთხოვთ, შეიყვანეთ სიმბოლო: ")


count = sentence.count(char)


print(f"სიმბოლო  გვხვდება წინადადებაში {count} ჯერ(ჯერ).")